package com.xml_project_be.xml_project.file.xml_file.reader;

public class TablesObject {
    private Object tables;
    private String name;
    private String content;
    private String settings;

    // Getter и Setter для tables
    public Object getTables() {
        return tables;
    }

    public void setTables(Object tables) {
        this.tables = tables;
    }

    // Getter и Setter для name
    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    // Getter и Setter для content
    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }

    // Getter и Setter для settings
    public String getSettings() {
        return settings;
    }

    public void setSettings(String settings) {
        this.settings = settings;
    }
}